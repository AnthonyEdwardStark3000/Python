# Slicing function example

vowels_list = ["A", "E", "I", "O", "U"]
print(vowels_list)
print("Calling slice function ... ")
print(vowels_list[1:4])
print("Calling with increment ...")
print(vowels_list[1:4:2])
print("Printing with an increment of 2 till last element ... ")
print(vowels_list[::2])
print("Printing with an increment of -1 till first element ... ")
print(vowels_list[::-1])
print("Slicing tuple")
example_tuple = ("ae", "ei", "ai", "oao")
print(example_tuple[1:3])
